    private void mostrarImagen(){ 
        String codigo;
        
        ResultSet rs = db.Listar("*", "usuario", "where Alias_usu='"+db.getUser()+"'");
        
        try{
            rs.next();
            Blob blob = (Blob) rs.getBlob("Imagen_usu");
            jTextArea1.setText(rs.getString("Notas_usu"));
            byte [] data = blob.getBytes(1,(int) blob.length());
            BufferedImage img = null;
            img = ImageIO.read(new ByteArrayInputStream(data));
            jXImageView1.setImage(img);
            jXImageView1.setScale(rs.getDouble("zoom_image"));
            zoom = rs.getDouble("zoom_image");
        } catch ( Exception ex){
            JOptionPane.showMessageDialog(rootPane, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }
    

    private void jMenuItem1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem1ActionPerformed
        /* Primero creo una tabla temporal */
        
        db.ConsActualizacion("drop table if exists tmpmax;"
                + "Create Table `tmpMax`("
                + "`Cod_producto` varchar(45) not null,"
                + "`cant_vendida` int(11) default null)"
                + "Engine= innodb default charset=latin1;");
        
        /*consulto por el rango de fechas para el reporte */
        
        jDViewDate a = new jDViewDate(this, true,"Seleccione una fecha de inicio:");
        a.setVisible(true);
        ResultSet r=null;
        
        jDViewDate b = new jDViewDate(this, true,"Seleccione una fecha de fin:");
        b.setVisible(true);

        /*consulto a la base de datos sobre los productos 
         * que fueron vendidos en el rango de fechas seleccionado*/
        
        ResultSet rs1,rs = null;
        rs=db.Listar("distinct Cod_inter_producto", "detalle_venta"," `Fecha_venta` >= '"+(a.fecha.getYear()+1900)+"-"
                  +(a.fecha.getMonth()+1)+"-"+(a.fecha.getDate())+"' and `Fecha_venta` <= '"+(b.fecha.getYear()+1900)+"-" 
                  +(b.fecha.getMonth()+1)+"-"+(b.fecha.getDate())+"'");
        try {
            /*Empiezo a cargar estos productos a la tabla temporal*/
            
            while(rs.next()){
                rs1=null;
                String prod = rs.getString("Cod_inter_producto");
                rs1=db.Listar("sum(cant_vendida)", "detalle_venta" , "where Cod_inter_producto= '"+prod+"'");
                rs1.next();
                
                db.Altas("tmpMax", "('"+prod+"','"+rs1.getInt("sum(cant_vendida)")+"')","");
            }
        } catch (SQLException ex) {
          JOptionPane.showMessageDialog(null,ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        
        /*hago la consulta ya con la tabla temporal para pasarle los datos al reporte*/
        ResultSet rs2=null;
        rs2=db.Listar("*", "vistamvend", "");
       

        JRResultSetDataSource jrRS = new JRResultSetDataSource(rs2);
        HashMap parameters = new HashMap();
        parameters.clear();
        
        parameters.put("Tipo_de_Reporte", "Producto más vendido");
        try{
            URL urlMaestro = getClass().getClassLoader().getResource("reportes/ReporteVentas.jasper");
            // Cargamos el reporte
            JasperReport masterReport = null;
            masterReport = (JasperReport) JRLoader.loadObject(urlMaestro);
            JasperPrint masterPrint = null;
            masterPrint = JasperFillManager.fillReport(masterReport,parameters,jrRS);
               
            
            
             JasperViewer ventana = new JasperViewer(masterPrint,false);
            ventana.setTitle("Vista Previa");
            ventana.setModalExclusionType(Dialog.ModalExclusionType.APPLICATION_EXCLUDE);
            ventana.setVisible(true);

        }catch(JRException e){
            e.printStackTrace();
            JOptionPane.showMessageDialog(null, "Ocurrio un error "+e.toString(),"ATENCION ", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jMenuItem1ActionPerformed

    private void autorizar(){
        String Nivel=user.getNivelUsuario();
        if(Nivel.equals("Administrador")){
            habilitador(true);
            jMAgrUsu.setEnabled(true);
            jmAgregarProveedor.setEnabled(true);
            jMproveedor.setEnabled(true);
            
        }else if(Nivel.equals("Depósito")){
            habilitador(false);
            jBProductos.setVisible(true);
            jMAgrUsu.setVisible(false);
            jMproveedor.setVisible(false);
            
        }else if(Nivel.equals("Ventas")){
            habilitador(false);
            jBProductos.setVisible(true);
            jBVentas.setVisible(true);
            jBCompra.setVisible(true);
            jBPresup.setVisible(true);
            jMAgrUsu.setVisible(false);
            jMproveedor.setVisible(false);
            jBPacientes.setVisible(false);
            
        }
    }
    
    public File obtenerArchivo(){
       int resultado = 0;
       JFileChooser elegir = new JFileChooser();
       
       FileView fv = new FileView() {};
       
       elegir.setFileView(fv);
       
       elegir.setFileSelectionMode(JFileChooser.FILES_ONLY);
       resultado = elegir.showOpenDialog(this);
       if(resultado == JFileChooser.CANCEL_OPTION){
           return null;
       }
       File archivo = elegir.getSelectedFile();
       if(archivo == null){
           JOptionPane.showMessageDialog(null, "Error al cargar archivo",
                        "Error", JOptionPane.ERROR_MESSAGE);
       }
       return archivo;
   }

    private void preparartabla() {
         r=null;
         r=db.Listar("*", "vistacompra3", "where Id_compra='"+id_compra+"'");
            //String titulos[]={"Cod. Cliente", "Nombre", "Telefono", "Direccion"};
            m =(new javax.swing.table.DefaultTableModel(
                new Object [][] {

                },
                new String [] {
                    "Cod. Producto", "Cant. Compra", "Descripcion", "Precio Compra", "Precio Total"
                }
            ) {
                boolean[] canEdit = new boolean [] {
                    false, false, false, false, false
                };

                public boolean isCellEditable(int rowIndex, int columnIndex) {
                    return canEdit [columnIndex];
                }
            });
            jTableClientes.setModel(m);
            String fila[] = new String[5];
            try {
            while(r.next()){
                fila[0]=r.getString("Cod_inter_producto");
                fila[1]=r.getString("Cant_adquirida");
                fila[2]=r.getString("Nom_producto")+" "+r.getString("Marca_producto")+" "+r.getString("detalle_producto");
                fila[3]=r.getString("Pre_compra");
                fila[4]=String.valueOf(Integer.valueOf(r.getString("Pre_compra"))*Integer.valueOf(r.getString("Cant_adquirida")));
                m.addRow(fila);
            }

        } catch (Exception ex) {
           JOptionPane.showMessageDialog(null, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void llenarcampos(){
      String id= jtNumCompra.getText();
      ResultSet rs = db.Listar("*", "vistacompra", "where Id_compra='"+id+"'");
        try {
            if(rs.next()){
                jFTFactura.setText(rs.getString("Factura_compra"));
                jTTipoFact.setText(rs.getString("Tipo_de_factura"));
                jTreceptor.setText(rs.getString("Nom_usu"));
                jXFecha.setDate(rs.getDate("Fecha_compra"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(jDDetalleCompra.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void calcultotal(){
        int filas = jTableClientes.getRowCount();
        
        for(int i =0; i<filas; i++){
            int pre= Integer.valueOf(String.valueOf(jTableClientes.getValueAt(i, 4)));
            precio+=pre;
        }
        jTPrecioTotal.setText(String.valueOf(precio));
    }
